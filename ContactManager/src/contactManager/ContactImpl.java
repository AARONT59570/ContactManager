package contactManager;

public class ContactImpl implements Contact {
	private String name;
	private int iD;
	private String notes;
	public ContactImpl(String name, int id, String notes)
	{
		this.name = name;
		this.iD = id;
		this.notes = notes;
	}
	public int getID()
	{
		return this.iD;
	}
	public String getName()
	{
		return this.name;
	}
	public String getNotes()
	{
		return this.notes;
	}
}
